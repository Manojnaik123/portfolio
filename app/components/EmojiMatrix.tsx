const ARTS = [
  { grid: ROCKET, label: "Rocket" },
  { grid: CAT, label: "Cat" },
  { grid: HOUSE, label: "House" },
  { grid: SPACESHIP, label: "Spaceship" },
];

const PixelArt = () => {
  const canvasRef = useRef<HTMLCanvasElement>(null);
  const [artIndex] = useState(() => Math.floor(Math.random() * ARTS.length));
  const art = ARTS[artIndex];

  useEffect(() => {
    const canvas = canvasRef.current;
    if (!canvas) return;
    const ctx = canvas.getContext("2d");
    if (!ctx) return;

    ctx.clearRect(0, 0, canvas.width, canvas.height);

    for (let row = 0; row < ROWS; row++) {
      for (let col = 0; col < COLS; col++) {
        const val = art.grid[row][col];
        if (val === 0) continue;
        ctx.fillStyle = PALETTE[val] ?? "#ccc";
        ctx.fillRect(col * CELL, row * CELL, CELL, CELL);
      }
    }
  }, [art]);

  return (
    <div className="flex flex-col items-center gap-1">
      <canvas
        ref={canvasRef}
        width={COLS * CELL}
        height={ROWS * CELL}
        style={{ imageRendering: "pixelated" }}
      />
      <span className="text-[10px] text-[#9b9b9b]">{art.label}</span>
    </div>
  );
};

export default PixelArt;